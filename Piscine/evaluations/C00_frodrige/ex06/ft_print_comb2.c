#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_comb2(void)
{
	int	a;
	int	b;

	a = -1;
	while (a < 98)
	{
		a++;
		b = a;
		while (b < 99)
		{
			b++;
			ft_putchar('0' + (a / 10));
			ft_putchar('0' + (a % 10));
			ft_putchar(' ');
			ft_putchar('0' + (b / 10));
			ft_putchar('0' + (b % 10));
			if (a != 98)
			{
				write(1, ", ", 2);
			}
		}
	}
}

int	main(void)
{
	ft_print_comb2();
return(0);
}
